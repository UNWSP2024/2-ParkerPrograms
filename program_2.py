#get the inputs of the ages of the five friends
age_1 = float(input('Enter the age of the first friend: '))
age_2 = float(input('Enter the age of the second friend: '))
age_3 = float(input('Enter the age of the third friend: '))
age_4 = float(input('Enter the age of the fourth friend: '))
age_5 = float(input('Enter the age of the fifth friend: '))

#calculate the average age
average_age = (age_1 + age_2 + age_3 + age_4 + age_5)/5

#print the average age
print(f'The average age of the friends is {average_age}.')
