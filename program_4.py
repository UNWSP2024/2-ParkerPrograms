#get the input of temperature in Celsius
celsius_temp = float(input('Enter the temperature in Celsius: '))

#convert to Fahrenheit
fahrenheit_temp = (9/5)*celsius_temp + 32

#print the temperature converted to Fahrenheit
print(f'This equals {fahrenheit_temp:,} degrees Fahrenheit.')
